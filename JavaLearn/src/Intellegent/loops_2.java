public class loops_2 {
    public static void main(String[] args) {

        for (int i = 1; i < 11; i++) {
            for (int j = 1; j <2 ; j++) {
                System.out.print(j+"x"+i+"="+(i*j));
            }
            System.out.println("");
        }
        
    }
}
