package Intellegent.StringBuilder_VarArgs;

public class string_num {
    public static void main(String[] args) {
        String numbers ="012345678";
        System.out.println("numbers.substring(1,3) = " + numbers.substring(1, 3));
        System.out.println("numbers.substring(7,7) = " + numbers.substring(7, 7));
        System.out.println("numbers.substring(7) = " + numbers.substring(7));
    }
}
